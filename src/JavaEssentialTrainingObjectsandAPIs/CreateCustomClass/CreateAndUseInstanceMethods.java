package JavaEssentialTrainingObjectsandAPIs.CreateCustomClass;

public class CreateAndUseInstanceMethods {
    /*
        Instance methods belong to an instance of a class than the class itself.
        if you were to not use the keyword static in a method, using that method out of the class will need you to
        make an instance of that class

        Instance methods need you to create an object in the class so that you
        can call the method.
        EX: Scanner scan = new Scanner(System.in);

        You then call on that instance to use the method
        String example = scan.getLine();

        creating an object though takes up data, so it is better to use static methods.
        But program complexity can get in the way of that.
     */
}
