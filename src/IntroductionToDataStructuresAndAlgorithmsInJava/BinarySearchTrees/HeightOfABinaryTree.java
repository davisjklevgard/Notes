package IntroductionToDataStructuresAndAlgorithmsInJava.BinarySearchTrees;

public class HeightOfABinaryTree {
    /*
        Height of  tree, assuming to be balanced, is determined by how many layers a tree has. so the height of a tree
        is called h with the bottom layer having 2^(h-1) nodes
        so all the nodes in the tree will be (2^(h)-1)/(2-1) = n
        h = O(log base 2 of n)
     */
}
