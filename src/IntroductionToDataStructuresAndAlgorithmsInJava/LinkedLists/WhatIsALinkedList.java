package IntroductionToDataStructuresAndAlgorithmsInJava.LinkedLists;

public class WhatIsALinkedList {
    /*
        Linked List
            An ordered set of data elements
            each element contains a link to its successor
            the list contains data elements and each element points to the next element
            The last element points to a null element
            This list can be used when making as new profile, you can add it to the list as an object.

        Data elements
            called nodes
        First elements
            called head element
        Node
            Node in a linked list
                can be divided into two parts
                    one part contains the data(any data we want to store)
                        can be primitive (int,double) or an object reference
                    the other holds the reference to the next node
                        it points to the next node
        Image viewer – Previous and next images are linked, hence can be accessed by next and previous button.

        Previous and next page in web browser – We can access previous and next url searched in web browser by pressing
        back and next button since, they are linked as linked list.

        Music Player – Songs in music player are linked to previous and next song. you can play songs either from
        starting or ending of the list.
     */
}
